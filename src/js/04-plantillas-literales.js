// Código de ejemplo de plantillas literales en JavaScript.
// las plantillas literales nos permiten interpolar variables y expresiones en una cadena de texto.

// ✅ Declarando variables
const nombre = "Alejandro";
const apellido = "Vargas";

// ✅ forma tradicional de concatenar strings

console.log("Hola, mi nombre es " + nombre + " " + apellido);

// ✅ forma moderna de concatenar strings con plantillas literales

console.log(`Hola, mi nombre es ${nombre} ${apellido}`);